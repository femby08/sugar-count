let currentLang = 'es';

export async function initI18n() {
  let conf = null;
  try {
    conf = await getConfig();
  } catch (e) {}

  if (conf && conf.lang && conf.lang !== 'auto') {
    currentLang = conf.lang;
  } else {
    // Autodetect
    const navLang = navigator.language.toLowerCase();
    if (navLang.startsWith('es')) currentLang = 'es';
    else if (navLang.startsWith('ca')) currentLang = 'ca';
    else if (navLang.startsWith('gl')) currentLang = 'gl';
    else if (navLang.startsWith('eu')) currentLang = 'eu';
    else currentLang = 'en';
  }

  applyTranslationsToDOM();
  return currentLang;
}

export function getCurrentLang() {
  return currentLang;
}

export function t(key, ...args) {
  const dict = dictionaries[currentLang] || dictionaries['es'];
  let text = dict[key] || dictionaries['en'][key] || key;
  
  if (args && args.length > 0) {
    args.forEach(arg => {
      text = text.replace('%s', arg);
    });
  }
  return text;
}

export function applyTranslationsToDOM(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.setAttribute('placeholder', t(key));
    } else {
      el.textContent = t(key);
    }
  });
  root.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
  });
  root.querySelectorAll('[data-i18n-option]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n-option'));
  });
}
